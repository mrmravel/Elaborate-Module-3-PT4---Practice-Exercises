public class Main {
    public static void main(String[] args) {
        // Create an integer array
        int[] numbers = {10, 20, 30, 40, 50};

        // Modify the second element (index 1)
        numbers[1] = 25;

        // Print the updated array
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }
    }
}
